package com.cg.entity;

public class Stu {

	public String name;
	public int rollno;
	public String Dept;
	public int age;

	public Stu() {
		
	}

	public Stu(String name, int rollno, String dept, int age) {
		super();
		this.name = name;
		this.rollno = rollno;
		Dept = dept;
		this.age = age;
	}
	
}
